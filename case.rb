current_weather = 'rainy'

case current_weather
when 'sunny'
	puts "Grab some shorts. Its warm!"
when 'cloudy'
	puts "Today its mellow. Bests to grab some coffee!"
when 'snowing'
	puts "Time to pull out those snow boots!"
when 'foggy'
	puts "Its going to be hard to see, drive carefully!"
else
	puts "Well, the weather is all over the place today!"
end